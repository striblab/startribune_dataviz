var chart;
nv.addGraph(function() {
// var width = 630, height = 350;
    chart = nv.models.multiBarChart()
      .transitionDuration(350)
      .reduceXTicks(true)  //If 'false', every single x-axis tick label will be rendered.
      .rotateLabels(0)      //Angle to rotate x-axis labels.
      .showControls(false)  //Allow user to switch between 'Grouped' and 'Stacked' mode.
      .groupSpacing(0.0)    //Distance between each group of bars.
      .showLegend(false)
      .stacked(true)
      .tooltip(function(key, x, y, e, graph) {
    return '<h3>' + key + '</h3>' +
          '<p>' +  y + ' pieces acquired in ' + x + '</p>';
        })
      .color(function(d){ 
        if (d.key == "Full MIA Collection"){return['#444'];} 
        else if (d.key == "African Art"){return['#00996f'];}
        else if (d.key == "Art from the Americas"){return['#d90d31'];}
        else if (d.key == "Oceanian Art"){return['#ffb818'];}
        else if (d.key == "Chinese, Indian and Southeast Asian Art"){return['#009ac8'];}
        else if (d.key == "Japanese and Korean Art"){return['#ff6600'];}
        else if (d.key == "Decorative Art, Textiles and Sculpture"){return['#7a9900'];}
        else if (d.key == "Paintings"){return['#850dd9'];}
        else if (d.key == "Photography and New Media"){return['#d90dd7'];}
        else if (d.key == "Prints and Drawings"){return['#18e7ff'];}

        else {return ['#98DF8A'];} 

      });
      // .width(width).height(height);

    chart.xAxis
        .tickFormat(d3.format(""));

    chart.yAxis
        .tickFormat(d3.format(',f'));

    d3.select('#chart1 svg')
        .datum(all_data)
        .call(chart);

    nv.utils.windowResize(chart.update);

    return chart;
});

